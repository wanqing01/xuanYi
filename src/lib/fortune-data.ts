export function getCurrentHour() {
  const hour = new Date().getHours()
  if (hour >= 23 || hour < 1) return hourLabels[0]
  if (hour >= 1 && hour < 3) return hourLabels[1]
  if (hour >= 3 && hour < 5) return hourLabels[2]
  if (hour >= 5 && hour < 7) return hourLabels[3]
  if (hour >= 7 && hour < 9) return hourLabels[4]
  if (hour >= 9 && hour < 11) return hourLabels[5]
  if (hour >= 11 && hour < 13) return hourLabels[6]
  if (hour >= 13 && hour < 15) return hourLabels[7]
  if (hour >= 15 && hour < 17) return hourLabels[8]
  if (hour >= 17 && hour < 19) return hourLabels[9]
  if (hour >= 19 && hour < 21) return hourLabels[10]
  return hourLabels[11]
}
