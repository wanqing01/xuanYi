// 根据日期生成稳定的随机种子
export function getDailySeededRandom(date: Date, offset: number = 0): number {
  const dateStr = `${date.getFullYear()}-${date.getMonth()}-${date.getDate()}-${offset}`
  let hash = 0
  for (let i = 0; i < dateStr.length; i++) {
    const char = dateStr.charCodeAt(i)
    hash = ((hash << 5) - hash) + char
    hash = hash & hash
  }
  return Math.abs(hash % 100) / 100
}

